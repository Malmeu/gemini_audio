export const TOP_DOWN_COMMUNICATION = `
Points-clés de la méthode
1. Hiérarchiser l'information avec la pyramide
Organiser ses idées en pyramide : conclusion principale en haut, suivie de sous-idées et de détails en dessous.
Trier les informations en groupes logiques (mutuellement exclusifs et collectivement exhaustifs).
Ne pas mélanger infos importantes et secondaires, pour éviter la confusion et faciliter la mémorisation.
2. Communication Top-Down
Communiquer en partant du haut de la pyramide : commencer par la conclusion ou le message clé.
Détailler ensuite les sous-points et arguments, seulement si le dirigeant demande plus d'explications.
Cette approche répond au temps limité et à la rapidité de réflexion des décideurs.
3. Logique déductive et inductive
Logique déductive : part d'une affirmation générale pour arriver à une conclusion spécifique (exemple classique du syllogisme).
Logique inductive : part de cas spécifiques ou observations pour aboutir à une conclusion générale.
Utiliser ces logiques permet de structurer son raisonnement et de convaincre avec clarté.
4. Clarté et concision
Aller à l'essentiel, éliminer les redondances et le jargon inutile.
Rendre le message percutant et facile à comprendre en peu de temps.
Préparer un "executive summary" qui capte immédiatement l'attention.
5. Exemples pratiques et storytelling
Illustrer ses idées avec des anecdotes et success stories pour marquer les esprits.
Proposer des exemples concrets en rapport avec les besoins et enjeux du décideur.
Le storytelling vient en complément pour rendre la communication vivante et mémorable.
`;

export const DISC_METHOD = `
Points-clés de la méthode DISC
1. Présentation générale
La méthode DISC classe les comportements en quatre profils principaux : Dominant (D), Influent (I), Stable (S), Consciencieux (C).
Chaque profil correspond à une combinaison d’orientation vers les tâches ou les relations, et d’extraversion ou introversion.
2. Les profils en détail
Dominant (Rouge) : Orienté tâches, assertif, direct, aime aller à l’essentiel, parfois perçu comme "pushy".
Influent (Jaune) : Extraverti, orienté relations, communicatif, chaleureux et enthousiaste, aime l’interaction sociale.
Stable (Vert) : Introverti, orienté relations, patient, fiable, préfère les échanges en tête-à-tête, attentif aux émotions.
Consciencieux (Bleu) : Introverti, orienté tâches, analytique, méthodique, aime l’ordre et les faits précis.
3. Utiliser DISC en prospection
Comprendre le profil du prospect permet d’adapter son langage, son ton, et son rythme.
Par exemple, un profil dominant appréciera un discours factuel et rapide, un profil influent préfèrera une approche chaleureuse et spontanée.
Identifier son propre profil aide aussi à mieux gérer ses forces et ses limites dans la relation commerciale.
`;

export const SPIN_SELLING = `
La méthode SPIN Selling
Les 4 catégories de questions SPIN
1. Questions de Situation (S)
Recueillir des informations sur le contexte, l’activité, la situation actuelle du prospect.
Exemple : "Quels outils utilisez-vous actuellement pour gérer X ?"
2. Questions de Problème (P)
Identifier les difficultés, obstacles ou insatisfactions du prospect.
Exemple : "Quels sont les défis majeurs dans votre processus actuel ?"
3. Questions d’Impact (I)
Faire prendre conscience au prospect des conséquences négatives des problèmes évoqués.
Exemple : "Quel impact ce problème a-t-il sur la performance de votre équipe ?"
4. Questions de Nécessité (N)
Aider le prospect à se projeter dans la solution et ses bénéfices.
Exemple : "Quels bénéfices attendez-vous en résolvant ce problème ?"
`;

